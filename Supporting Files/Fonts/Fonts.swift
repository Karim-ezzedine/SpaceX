import UIKit


func getLatoFont(size: CGFloat) -> UIFont {
    return UIFont(name: "Lato-Regular", size: size)!
}

func getMediumLatoFont(size: CGFloat) -> UIFont {
    return UIFont(name: "Lato-Medium", size: size)!
}

func getBoldLatoFont(size: CGFloat) -> UIFont {
    return UIFont(name: "Lato-Bold", size: size)!
}

func getExtraBoldLatoFont(size: CGFloat) -> UIFont {
    return UIFont(name: "Lato-Black", size: size)!
}
